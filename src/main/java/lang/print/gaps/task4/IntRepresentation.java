package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';
        int c2 = (int)c;
        int h2 = (int)h;
        int a2 = (int)a;
        int r2 = (int)r;
		System.out.println(c2);
		System.out.println(h2);
		System.out.println(a2);
		System.out.println(r2);
    }
}
